/* Private variables ---------------------------------------------------------*/
SPI_HandleTypeDef hspi1;
SPI_HandleTypeDef hspi2;
DMA_HandleTypeDef hdma_spi1_rx;
DMA_HandleTypeDef hdma_spi2_tx;

USBD_CUSTOM_HID_HandleTypeDef *hhid;

extern uint16_t USB_RequestIndexI;      // Request  Index In
extern uint16_t USB_RequestIndexO;      // Request  Index Out

extern uint16_t USB_ResponseIndexI;     // Response Index In
extern uint16_t USB_ResponseIndexO;     // Response Index Out

extern uint8_t  USB_Request [USB_PACKET_COUNT][DAP_PACKET_SIZE + 1];  // Request  Buffer
extern uint8_t  USB_Response[USB_PACKET_COUNT][DAP_PACKET_SIZE + 1];  // Response Buffer
    
/* Private function prototypes -----------------------------------------------*/
void SystemClock_Config(void);
void Error_Handler(void);
static void MX_GPIO_Init(void);
static void MX_GPIO_Init(void);
static void MX_DMA_Init(void);
static void MX_SPI1_Init(void);
static void MX_SPI2_Init(void);

int main(void)
{
  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_SPI1_Init();
  MX_SPI2_Init();
  MX_USB_DEVICE_Init();
  
  HAL_SPI_Receive_DMA(&hspi1, USB_Response[USB_ResponseIndexI], DAP_PACKET_SIZE);

  /* Infinite loop */
  while (1)
  {
    hhid = (USBD_CUSTOM_HID_HandleTypeDef*) hUsbDeviceFS.pClassData;
    
    if ((USB_ResponseIndexI != USB_ResponseIndexO) && (hhid->state == CUSTOM_HID_IDLE))
    {
      // HID Send
      USBD_CUSTOM_HID_SendReport(&hUsbDeviceFS, USB_Response[USB_ResponseIndexO], DAP_PACKET_SIZE);
      USB_ResponseIndexO = (USB_ResponseIndexO + 1) % USB_PACKET_COUNT;
    }

    if ((USB_RequestIndexI != USB_RequestIndexO) && (HAL_SPI_GetState(&hspi2) == HAL_SPI_STATE_READY))
    {
      // SPI2_Send
      HAL_GPIO_WritePin(GPIOB, GPIO_PIN_12, GPIO_PIN_RESET);
      HAL_SPI_Transmit_DMA(&hspi2, USB_Request[USB_RequestIndexO], DAP_PACKET_SIZE);
      USB_RequestIndexO = (USB_RequestIndexO + 1) % USB_PACKET_COUNT;
    }
  }
}

/** EXTI line detection callback.
*/
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
  if(GPIO_Pin == GPIO_PIN_4)
  {
    HAL_SPI_DMAStop(&hspi1);
    USB_ResponseIndexI = (USB_ResponseIndexI + 1) % USB_PACKET_COUNT;
    HAL_SPI_Receive_DMA(&hspi1, USB_Response[USB_ResponseIndexI], DAP_PACKET_SIZE + 1);
  }
}

/** Tx Transfer completed callback.
*/
void HAL_SPI_TxCpltCallback(SPI_HandleTypeDef *hspi)
{
  if(hspi->Instance==SPI2)
  {
    HAL_GPIO_WritePin(GPIOB, GPIO_PIN_12, GPIO_PIN_SET);
  }
}

/** System Clock Configuration
*/
void SystemClock_Config(void)
{

  RCC_OscInitTypeDef RCC_OscInitStruct;
  RCC_ClkInitTypeDef RCC_ClkInitStruct;
  RCC_PeriphCLKInitTypeDef PeriphClkInit;

  RCC_OscInitStruct.OscillatorType = RCC_OSCILLATORTYPE_HSI|RCC_OSCILLATORTYPE_HSI48;
  RCC_OscInitStruct.HSIState = RCC_HSI_ON;
  RCC_OscInitStruct.HSI48State = RCC_HSI48_ON;
  RCC_OscInitStruct.HSICalibrationValue = 16;
  RCC_OscInitStruct.PLL.PLLState = RCC_PLL_ON;
  RCC_OscInitStruct.PLL.PLLSource = RCC_PLLSOURCE_HSI;
  RCC_OscInitStruct.PLL.PLLMUL = RCC_PLL_MUL4;
  RCC_OscInitStruct.PLL.PREDIV = RCC_PREDIV_DIV1;
  if (HAL_RCC_OscConfig(&RCC_OscInitStruct) != HAL_OK)
  {
    Error_Handler();
  }

  RCC_ClkInitStruct.ClockType = RCC_CLOCKTYPE_HCLK|RCC_CLOCKTYPE_SYSCLK|RCC_CLOCKTYPE_PCLK1;
  RCC_ClkInitStruct.SYSCLKSource = RCC_SYSCLKSOURCE_PLLCLK;
  RCC_ClkInitStruct.AHBCLKDivider = RCC_SYSCLK_DIV1;
  RCC_ClkInitStruct.APB1CLKDivider = RCC_HCLK_DIV1;
  if (HAL_RCC_ClockConfig(&RCC_ClkInitStruct, FLASH_LATENCY_1) != HAL_OK)
  {
    Error_Handler();
  }

  PeriphClkInit.PeriphClockSelection = RCC_PERIPHCLK_USB;
  PeriphClkInit.UsbClockSelection = RCC_USBCLKSOURCE_HSI48;
  if (HAL_RCCEx_PeriphCLKConfig(&PeriphClkInit) != HAL_OK)
  {
    Error_Handler();
  }

  HAL_SYSTICK_Config(HAL_RCC_GetHCLKFreq()/1000);

  HAL_SYSTICK_CLKSourceConfig(SYSTICK_CLKSOURCE_HCLK);

  /* SysTick_IRQn interrupt configuration */
  HAL_NVIC_SetPriority(SysTick_IRQn, 0, 0);
}


